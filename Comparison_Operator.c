#include <stdio.h>

void main(){
    
    /**type data int dengan variable a diisi dengan nilai 4
    variable b diisi dengan nilai 3**/
    int a = 4;
    int b = 3;
   
    // keterangan
    printf("a = %d\n", a);
    printf("b = %d\n", b);
    
    // operator pembanding
    printf("a > b = %d\n", a > b);
    printf("a < b = %d\n", a < b);
    printf("a >= b = %d\n", a >= b);
    printf("a <= b = %d\n", a <= b);
    printf("a == b = %d\n", a == b);
    printf("a != b = %d\n", a != b);
}
